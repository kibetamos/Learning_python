# Creating a tuple
my_tuple = (1, "hello", 3.14, True)

# Accessing elements in a tuple
print(my_tuple[0])  # Output: 1
print(my_tuple[1])  # Output: hello

# Tuple unpacking
a, b, c, d = my_tuple
print(a)  # Output: 1
print(b)  # Output: hello

